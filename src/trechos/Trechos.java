package trechos;

public class Trechos {
    public String introduzirPsicopata() {
        return """
                Psicopata: Que merda você fez, hein, jovem? Não tem noção das consequências que suas ações podem \
                causar. E agora está aqui para aprender a nunca mais fazer isso. Pessoas como você não têm salvação.

                Fiquei olhando pra ele, procurando entender o que eu tinha feito. Ele ficou calado. Me lembrei agora \
                que eu tava meio bêbado em uma festa, acho que já ia voltar pra casa quando... Ele me raptou... E nem \
                lembro como foi isso. Mas se eu não me engano eu lembro de um carro. Meus pensamentos foram \
                interrompidos quando ele voltou a falar.

                Psicopata: Você estava em um estado totalmente deplorável quando saiu daquela festa: bêbado e drogado. \
                Fazendo coisas absurdas.
                Eu: O quê?! Apenas bebi um pouco. Nada mais!
                Psicopata: Ah, sim. Com certeza. Aposto que você tinha bebido só um pouquinho para estar daquele \
                jeito, moleque deliquente.
                Eu: O que eu fiz de tão grave pra você me prender aqui?
                Psicopata: Você ainda pergunta?! Já não basta ser um drogado de merda? Tenho nojo desse lixo de \
                geração. E ainda quando fazem merda querem ser vistos como a vítima. Mas se depender de mim não. Se a \
                sociedade não dá um jeito em tipos como você, eu dou! E gente como você tem mais é que se fuder \
                mesmo. Matar é pouco para o que vocês merecem. Vocês precisam sofrer muito primeiro.
                Eu: Por favor, não! Eu só bebi, não fiz mais nada! Eu juro!
                Psicopata: Eu não acredito. Só pelo lugar em que estava cheio de lixos que nem você já são motivos \
                suficiente para eu fazer o que vou fazer hoje. O mundo seria um lugar melhor sem vocês. Eu não posso \
                matar todos, mas pelo menos vou fazer minha parte na purificação.

                Percebi que não adiantava mais falar. Esse filho da puta é louco e quer me matar a qualquer custo. \
                Para eu escapar daqui teria que ser por conta própria.""";
    }

    public String fugir() {
        return "A casa não tinha muros. Comecei a fugir e vi que ficava em um lugar isolado de frente para o mar. " +
                "Resolvi ir em direção a coisa mais próxima que havia naquela casa. Um morro cheio de árvores e mato." +
                " Se o assassino tivesse uma arma de fogo, lá seria o único lugar que eu conseguiria me esconder ou" +
                " fugir.";
    }

    public String fugirSemStealth() {
        return fugir() + "\nO psicopata abriu a porta e correu na minha direção.";
    }

    public String fugirComSucesso() {
        return "Mas não conseguiu me alcançar, afinal, eu já estava bem longe. Só ouvi ele dizer bem distante e " +
                "cansado:";
    }

    public String passarCuidadosamente() {
        return "Passei tomando cuidado. Acho que foi o melhor porque enquanto andava pelo telhado da varanda, senti " +
                "que estava meio frágil. Me preparei para pular no chão. O assassino botou a cara na janela e só deu" +
                " tempo de me ver pulando.";
    }

    public String passarRapidamente() {
        return "Pulei com tudo pela janela. Cai no telhado e ele cedeu. Senti uma forte dor na perna: Tinha " +
                "quebrado. Mas, foda-se, na adrenalina eu consegui correr aos pulos mesmo com dor.";
    }

    public String serPego() {
        return "Ele era velho, sua velocidade era lenta, mas com minha perna quebrada, eu estava mais lento que ele. " +
                "Então, infelizmente, ele conseguiu me alcançar e me derrubou no chão cheio de mato. Comecei a " +
                "gritar. Ele pegou a faca e tentou enfiar no meu peito. Eu segurei a mão dele. Ele estava em cima de" +
                " mim cada vez mais fazendo força para a faca me perfurar. Seus olhos estavam cheio de fúria. Ele me " +
                "disse:";
    }

    public String serMorto() {
        return "Ele começou a me esfaquear em todas as partes do meu corpo. Totalmente, agoniado," +
                " ainda tentei me defender. Mas nada adiantava. O corte no pescoço não me deixava gritar. Eu" +
                " sangrava cada vez mais. Queria que um milagre acontecesse... ";
    }

    public String verAssassinoMorto() {
        return "Fiquei encarando o corpo morto. Estava de olhos abertos olhando para mim. Fiquei mal. Mas, então " +
                "lembro que ele iria fazer bem pior comigo... Acho que fiz o que deveria ser feito.";
    }

    public String finalFugirComSucesso() {
        return "Sei mais ou menos onde fica a festa em que eu estava. Não me arrisco a andar pela estrada. Pode ser " +
                "que ele use o carro dele para me procurar.";
    }
}
